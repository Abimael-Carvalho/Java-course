package br.com.abim.introducao.arrays;

public class Arrays {
    public static void main(String[] args) {
        int[] idade = new int[3];
        idade[0] = 20;
        idade[1] = 15;
        idade[2] = 30;
        System.out.println("A idade 1 é: "+idade[0]);
        System.out.println("A idade 2 é: "+idade[1]);
        System.out.println("A idade 3 é: "+idade[2]);
    }
}
